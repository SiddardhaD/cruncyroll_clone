    String animeTitle,
  ) {
    // Multiple working video URLs to test different sizes and sources
    final videoUrls = [
      'assets/videos/video1.mp4',
      'assets/videos/video1.mp4',
      'https://www.papytane.com/mp4/accrobra.mp4',
      'https://www.papytane.com/mp4/accrobra.mp4',
      'https://www.papytane.com/mp4/accrobra.mp4',
      'https://www.papytane.com/mp4/accrobra.mp4',
    ];

    return List.generate(count, (index) {
      final episodeNumber = index + 1;
      // Cycle through different videos for variety
      final videoUrl = videoUrls[episodeNumber % videoUrls.length];

      return {
        'id': '$animeId-ep$episodeNumber',
        'animeId': animeId,
        'episodeNumber': episodeNumber,
        'title': 'Episode $episodeNumber: ${_getEpisodeTitle(episodeNumber)}',
        'description':
            'In this episode, the story continues with exciting developments and character growth. Watch as the heroes face new challenges and overcome obstacles.',
        'thumbnailUrl':
            'https://picsum.photos/seed/$animeId-ep$episodeNumber/400/225',
        'videoUrl': videoUrl,
        'durationInSeconds': 1440, // 24 minutes
        'releaseDate': DateTime.now()
            .subtract(Duration(days: count - episodeNumber))
            .toIso8601String(),
        'isWatched': episodeNumber <= 2, // First 2 episodes marked as watched
        'watchedProgress': episodeNumber == 1
            ? 1440
            : (episodeNumber == 2 ? 720 : null),
      };
    });
